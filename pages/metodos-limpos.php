        <h5>Energia Hidrelétrica</h5>
        <img src="../img/metodos-limpos/energia-hidro.png" width="50%" alt="">
        <p>Energia Hidrelétrica, energia hidráulica ou energia hídrica é a energia obtida a partir da energia potencial de uma massa de água. A forma na qual ela se manifesta na natureza é nos fluxos de água, como rios e lagos e pode ser aproveitada por meio de um desnível ou queda d'água.
        Pode ser convertida na forma de energia mecânica (rotação de um eixo) através de turbinas hidráulicas ou moinhos de água. As turbinas por sua vez podem ser usadas como acionamento de um equipamento industrial, como um compressor, ou de um gerador elétrico, com a finalidade de prover energia elétrica para uma rede de energia.</p>
        <p>É necessário que haja um fluxo de água para que a energia seja gerada de forma contínua no tempo, por isto embora se possa usar qualquer reservatório de água, como um lago, deve haver um suprimento de água ao lago, caso contrário haverá redução do nível e com o tempo a diminuição da potência gerada (ver equação acima).
        As represas (barragens) são nada mais que lagos artificiais, construídos num rio, permitindo a geração contínua. No Brasil, devido a sua enorme quantidade de rios, a maior parte da energia elétrica disponível é proveniente de grandes usinas hidrelétricas.</p>
