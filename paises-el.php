                        energia. Nos últimos anos, os projetos de uso de fontes de energias renováveis no país têm recebido
                        sucessivos investimentos do setor privado.
                        A Índia tem sido pioneira na utilização da energia renovável descentralizada (ERD). O governo da
                        Índia estabeleceu metas para reduzir a emissão total de carbono projetada da Índia em um bilhão de
