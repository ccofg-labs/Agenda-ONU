                        outubro de 2020, o país estava caminhando para uma geração de energia solar consideravelmente maior
                        comparado ao ano de 2019.
                        Cada vez mais, a população alemã exige políticas públicas que busquem diminuir o impacto que a
                        sociedade vem causando ao meio ambiente. Por isso, as questões climáticas têm se tornado
                        constantemente uma das principais discussões políticas dentro do país.
