                        A matriz energética brasileira é uma das mais renováveis entre todos os países com as grandes
                        economias mundiais, 48% da nossa matriz é renovável. Para você ter uma ideia, a média mundial é de
                        14% e se compararmos com os países mais desenvolvidos, por exemplo, os países que fazem parte da
                        OCDE [Organização para a Cooperação e Desenvolvimento Econômico], essa participação é ainda menor, é
                        11%. E o que significa 48% de renováveis na matriz? Significa que toda a energia produzida e
                        consumida no Brasil é originária de fontes energéticas renováveis, como o sol, o vento, a água e a
                        biomassa. E se analisarmos agora a matriz de energia elétrica, a renovabilidade da nossa matriz é
                        ainda maior. Em 2020, terminamos o ano com 85% da nossa matriz renovável, enquanto a média mundial é
                        de apenas 28%. Isso demonstra a importância da nossa matriz e nos deixa orgulhosos como brasileiros
                        de ter uma matriz tão renovável.
