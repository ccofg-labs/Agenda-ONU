                        Os Estados Unidos atingiram um marco histórico no setor de energia. Após 134 anos de reinado do
                        carvão como a principal fonte energética, as energias renováveis se tornaram as mais consumidas
                        pelos americanos, no ano de 2019. A conta leva em consideração todas as modalidades de geração
                        limpa, como eólica, solar e hidrelétrica.
                        Em outro marco histórico, a energia eólica ultrapassou a hidrelétrica como a principal fonte limpa
                        dos Estados Unidos. O crescimento das renováveis se deve quase inteiramente ao avanço das energias
                        provenientes do sol e do vento.
                        Nos últimos anos, as fontes renováveis de energia, Solar e eólica, responderam por mais da metade da
                        nova capacidade instalada no país. Isso porque, devido ao baixo preço do gás natural, muitas usinas
                        a carvão foram desativadas, sendo substituídas pelas usinas solares e eólicas, as quais se tornam
                        cada vez mais acessíveis.
                        Em 2021, o governo dos Estados Unidos estabeleceu um plano para que a energia solar seja responsável
                        por garantir 45% do fornecimento elétrico do país até 2050, meta que visa transformar o setor
                        nacionalmente e combater a crise climática global.
